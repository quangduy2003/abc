    <h3>4, Bán mỹ phẩm phá giá:</h3>
      <img src="https://i0.wp.com/blogdeptunhien.com/wp-content/uploads/2019/08/cac-hang-my-pham-noi-tieng-nhat-the-gioi-1.jpg?fit=800%2C360&ssl=1" alt="alt"/>
      <p>Bán phá giá thực tế là một khái niệm cơ bản để chỉ việc các sản phẩm được bán ở một thị trường với giá bán ở dưới mức giá thành sản xuất. Bán phá giá thường được thể hiện qua nhiều cách khác nhau để hạ giá, tăng khả năng cạnh tranh, đánh bại đối thủ và chiếm lĩnh thị trường ngay cả khi phải chấp nhận bán lỗ ở một mức nào đó. Dưới góc độ pháp luật cạnh tranh, hành vi bán phá giá được xem là bất hợp pháp. <p>
      Đối với quan hệ thương mại quốc tế, bán phá giá là một hành vi của doanh nghiệp thuộc quốc gia này bán sang quốc gia khác với giá quá thấp nhằm giành giật thị trường xuất khẩu. Rõ ràng các mặt hàng khi nhập khẩu có giá thấp hơn và giá bán cũng thấp hơn cả giá thị trường thì người bị thiệt đầu tiên sẽ là các nhà sản xuất trong nước. Đó là lý do mà bán phá giá được đánh giá là hành vi cạnh tranh không công bằng, phải được ngăn chặn để đảm bảo tuyệt đối quyền lợi người kinh doanh cũng như người tiêu dùng. <p>
      Theo đó, Hiệp định chung về thuế quan và mậu dịch, Hiệp định chống bán phá giá của WTO đã cho phép các quốc gia thành viên được quyền đưa ra các biện pháp thích hợp nhằm bảo vệ ngành sản xuất trong nước khi những ngành này bị thiệt hại thực sự từ hành vi bán phá giá của các đối tác nước ngoài. 

Tại Việt Nam, Ủy ban thường vụ Quốc Hội đã ban hành pháp lệnh chống bán phá giá hàng hóa nhập khẩu vào Việt Nam có hiệu lực từ ngày 01/10/2004 quy định các biện pháp cụ thể mà chính phủ Việt Nam có thể áp dụng khi có hành vi bán phá giá từ phía đối tác nước ngoài vào thị trường Việt Nam. Pháp lệnh này căn bản tuân thủ các quy định tại Điều 6 Hiệp định GATT và Hiệp định chống phá giá của WTO.<p>
Theo đó, hàng hóa có xuất xứ từ nước ngoài bị coi là bán phá giá khi nhập khẩu vào Việt Nam nếu hàng hóa đó được bán với giá thấp hơn giá thông thường. Trong trường hợp không có hàng hóa tương tự được bán trên thị trường nội địa của nước (vùng lãnh thổ) xuất khẩu hoặc có hàng hóa tương tự được bán trên thị trường nội địa của nước hoặc vùng lãnh thổ xuất khẩu nhưng với khối lượng, số lượng hoặc trị giá hàng hóa không đáng kể thì giá thông thường của hàng hoá nhập khẩu vào Việt Nam được xác định theo một trong hai cách sau:<p>

Giá có thể so sánh được của hàng hóa tương tự của nước (vùng lãnh thổ) xuất khẩu đang được bán trên thị trường một nước thứ ba trong điều kiện thương mại thông thường.<p>
Giá thành hợp lý của hàng hóa cộng thêm các chi phí hợp lý khác và lợi nhuận ở mức hợp lý, xét theo từng công đoạn từ sản xuất đến lưu thông trên thị trường của nước hoặc vùng lãnh thổ xuất khẩu hoặc nước thứ 3.  <p>
