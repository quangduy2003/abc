    <h3>2, Thị trường mỹ phẩm hàn quốc </h3>
      <img src="https://foxcosmetics.vn/wp-content/uploads/2021/06/phan-nuoc-trang-diem-han-quoc-clio-kill-cover-liquid-founwear.jpg" alt="alt"/>
      <p>Theo Cục Sở hữu trí tuệ Hàn Quốc (KIPO), các thương hiệu mỹ phẩm của quốc gia này đang phải vật lộn với nhiều vụ làm giả thương hiệu và mẫu thiết kế từ Trung Quốc và các nước Đông Nam Á.

Theo báo Korea Times, thống kê vào năm 2021 cho thấy mỹ phẩm Hàn bị vi phạm thương hiệu nhiều nhất ở Trung Quốc với 2.920 vụ, tiếp theo là Indonesia với 840 vụ, Việt Nam có 660 vụ, và 550 vụ tại Thái Lan.

Mỹ phẩm nhái được sao chép nhận diện thương hiệu hoặc thiết kế của sản phẩm gốc, sau đó bán cho người tiêu dùng với giá thấp hơn. Các công ty Hàn Quốc đã kêu gọi cộng đồng quốc tế ngăn chặn vi phạm quyền sở hữu trí tuệ nhưng doanh nghiệp tại các quốc gia được đề cập lại tỏ ra thờ ơ.

Như trường hợp sản phẩm phấn nước trang điểm Kill Cover từ thương hiệu Clio (Hàn Quốc) bị một thương hiệu có tên gần giống là Pony Clio từ Trung Quốc làm nhái với tên mới là Kiss Cover.

Sản phẩm gel có thành phần 99% từ nha đam của Công ty Holika Holika cũng đã xuất hiện hàng nhái. Các sản phẩm này được bán khắp Trung Quốc và các quốc gia Đông Nam Á.
Các bên trung gian ở Trung Quốc đã đăng ký quyền sở hữu trí tuệ cho các sản phẩm nhái trước cả các công ty chính hãng vì họ hiểu quy trình chứng nhận an toàn vệ sinh phức tạp tại nước sở tại.

Quy định về chứng nhận an toàn vệ sinh tại Ủy ban Y tế quốc gia Trung Quốc yêu cầu phía đơn vị đăng ký phải cung cấp danh sách liệt kê đủ các nguyên liệu cũng như công nghệ cấu thành sản phẩm, quy trình đăng ký cũng rất phức tạp, khiến các hãng mỹ phẩm Hàn gặp khó.
Số lượng các bên trung gian ở Trung Quốc đăng ký quyền sở hữu thương hiệu của các hãng mỹ phẩm Hàn Quốc đã tăng lên trong 3 năm qua. Các bên trung gian này sẽ bán lại quyền sở hữu thương hiệu cho các công ty Hàn Quốc hoặc sử dụng thương hiệu đã đăng ký để bán hàng nhái cho người tiêu dùng dưới dạng hàng chính hãng ở Trung Quốc.

Vì vậy, các thương hiệu Hàn Quốc khó bán sản phẩm của họ ở Trung Quốc thông qua kênh chính thức, mà phải phụ thuộc vào kênh mua sắm trực tuyến tại Hàn hoặc thông qua nhà buôn Trung Quốc.

“Chúng tôi nhận thấy đơn đặt hàng cho các sản phẩm phổ biến của chúng tôi tại Trung Quốc và Việt Nam giảm dần qua các năm”, đại diện một hãng mỹ phẩm Hàn Quốc cho biết. “Lý do chính là vì bên trung gian lúc đầu thường cho người mua thấy họ có sản phẩm chính hãng của chúng tôi, nhưng sau đó họ bán hàng nhái cho người tiêu dùng. Các hãng mỹ phẩm Hàn Quốc gần như không thể làm gì được".
